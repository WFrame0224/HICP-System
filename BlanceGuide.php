					<h2 class="guide-title">二、时钟练习</h2>
					<p>
						<ul class="am-list am-list-static">
							<p class="guide-detail-title">操作步骤：</p>
							<li>1、用左手扶住椅子，单腿站立；</li>
							<li>2、想象一个时钟，身前是12点身后是6点。将右臂带到12点、3点、6点钟位置</li>
							<li>3、换边练习</li>
							<div class="guide-attention">
								注意：
								<ol>
									<li>在尝试这项练习时，一定要抓住椅子以防摔倒</li>
									<li>如果你的肩膀有疼痛，就不需要伸展到太远（如果达不到6点位置，只做到3点位置即可）</li>
									<li>切记量力而为</li>
								</ol>
							</div>
						</ul>
						<br>

					</p>
				</div>
